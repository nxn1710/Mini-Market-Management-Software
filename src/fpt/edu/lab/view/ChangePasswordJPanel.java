    private void jbtnChangePasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnChangePasswordActionPerformed
        String password = jtfOldPassword.getText();
        String passwordChange = jtfNewPassword.getText();
        String passwordConfirm = jtfConfirmPassword.getText();
        //kiá»ƒm tra passwordChange == passwordConfirm
        if (passwordChange.equals(passwordConfirm)) {
            if (UserDao.changePassword(passwordChange, password, user.getUsername()) == true) {
                JOptionPane.showMessageDialog(this, bundle.getString("joChangPassSuccess"));
            } else {
                JOptionPane.showMessageDialog(this, bundle.getString("joChangePassFail"));
            }
        } else {
            JOptionPane.showMessageDialog(this, bundle.getString("joSamePass"));
        }
    }//GEN-LAST:event_jbtnChangePasswordActionPerformed


