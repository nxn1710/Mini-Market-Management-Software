    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        DefaultTableModel tableModel = (DefaultTableModel) this.tableListUser.getModel();
        ArrayList<User> list = null;
        list = UserDao.search(txtSearchFullname.getText(), cbRolename.getSelectedItem().toString());
        while (tableModel.getRowCount() > 0) {
            tableModel.removeRow(0);
        }
        for (User user : list) {
            tableModel.addRow(new Object[]{user.getUsername(), user.getFullname(), user.getPassword(), user.getRolename()});
        }
    }//GEN-LAST:event_btnSearchActionPerformed
    public void loadToUserTable() {
        DefaultTableModel tableModel = (DefaultTableModel) this.tableListUser.getModel();
        while (tableModel.getRowCount() > 0) {
            tableModel.removeRow(0);
        }

        listUser = UserDao.getAllUser();
        for (User user : listUser) {
            tableModel.addRow(new Object[]{user.getUsername(), user.getFullname(), user.getPassword(), user.getRolename()});
        }
    }

    public int getIndexRole(String rolename) {
        int pos = -1;
        for (int i = 0; i < listRole.size(); i++) {
            if (listRole.get(i).getRoleName().equalsIgnoreCase(rolename)) {
                return i;
            }
        }
        return pos;
    }

    public void getRoleName() {
        listRole = RoleDao.getAllRole();
        for (Role role : listRole) {
            cbRole.addItem(role.getRoleName());
            cbRolename.addItem(role.getRoleName());
        }
    }

