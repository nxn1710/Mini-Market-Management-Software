    private void btnUserNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUserNewActionPerformed
        String username = txtUsername.getText();
        String password = txtPassword.getText();
        String fullname = txtFullname.getText();
        int indexRoleSelect = cbRole.getSelectedIndex();
        String rolename = listRole.get(indexRoleSelect).getRoleName();
        if (username.equals("") || password.equals("") || fullname.equals("")) {
            JOptionPane.showMessageDialog(getRootPane(), bundle.getString("joFillIn"));
        } else if (UserDao.isDuplicate(username)) {
            JOptionPane.showMessageDialog(getRootPane(), bundle.getString("joCheckExistUser"));
        } else {
            User user = new User(username, fullname, password, rolename);
            UserDao.addUser(user, indexRoleSelect);
            loadToUserTable();
        }
    }//GEN-LAST:event_btnUserNewActionPerformed

    private void btnUserUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUserUpdateActionPerformed
        String username = txtUsername.getText();
        String password = txtPassword.getText();
        String fullname = txtFullname.getText();
        String rolename = String.valueOf(cbRole.getSelectedIndex() + 1);
        User user = new User(username, fullname, password, rolename);
        UserDao.updateUser(username, user);
        loadToUserTable();
    }//GEN-LAST:event_btnUserUpdateActionPerformed

